/** Длительность осады зоны в секундах (A4) */
export function siegeSeconds(neutral: boolean, def: number): number {
    const ms = neutral
        ? CFG.SIEGE_NEUTRAL_MS
        : CFG.SIEGE_BASE_MS + Math.min(def, CFG.SIEGE_DEF_CAP) * CFG.SIEGE_PER_DEF_MS;
    return ms / 1000;
}